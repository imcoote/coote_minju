"""*******************************************************************************
 * 소요시간: 10분
 * 시간복잡도: O(N log N)
 * 메모리: 32412 kb
 * 시간: 36 ms
 *******************************************************************************"""
n = int(input())

a = list(map(int, input().split()))
b = list(map(int, input().split()))

a.sort() # 오름차순 정렬
b.sort(reverse = True) # 내림차순 정렬

sum = 0
for i in range(n):
    sum += a[i] * b[i] # a의 최소값과 b의 최대값을 곱하여 합산

print(sum)